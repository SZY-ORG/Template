package com.shizy.template.common.bean;

/**
 * 取值范围
 */
public class Range extends BaseBean {

	private String min;
	private String max;

	public String getMin() {
		return min;
	}

	public void setMin(String min) {
		this.min = min;
	}

	public String getMax() {
		return max;
	}

	public void setMax(String max) {
		this.max = max;
	}
}
